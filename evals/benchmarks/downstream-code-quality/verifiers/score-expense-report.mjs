class ScorerError extends Error {
  constructor(kind, code) {
    super(code);
    this.kind = kind;
    this.code = code;
  }
}

function fail(kind, code) {
  throw new ScorerError(kind, code);
}

function operational(code) {
  fail("operational-failure", code);
}

