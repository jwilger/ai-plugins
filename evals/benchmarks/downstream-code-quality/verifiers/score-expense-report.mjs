function parseSandboxStatus(serialized) {
  const events = serialized
    .trim()
    .split("\n")
    .filter(Boolean)
    .map((line) => JSON.parse(line));
  const childStarted = events.some((event) =>
    Object.hasOwn(event, "child-pid"),
  );
  const exitEvent = events.findLast((event) =>
    Object.hasOwn(event, "exit-code"),
  );
  return { childStarted, exitCode: exitEvent?.["exit-code"] };
}

function runCargo(
  target,
  command,
  artifactDirectory,
  executionKind = "candidate",
) {
  const result = spawnSync(
    target.bwrap,
    sandboxArgs({ ...target, artifactDirectory, command }),
    {
      encoding: "utf8",
      env: {},
      killSignal: "SIGKILL",
      maxBuffer: sandboxLimits.outputBytes,
      stdio: ["ignore", "pipe", "pipe", "pipe"],
      timeout: sandboxLimits.timeoutMilliseconds,
    },
  );
  if (result.error) {
    if (["ETIMEDOUT", "ENOBUFS"].includes(result.error.code)) {
      if (executionKind === "canary") {
        operational("toolchain-canary-resource-failure");
      }
      safety("candidate-resource-limit");
    }
    operational("sandbox-process-failed");
  }
  let status;
  try {
    status = parseSandboxStatus(result.output[3] || "");
  } catch {
    operational("sandbox-status-invalid");
  }
  if (
    !status.childStarted ||
    status.exitCode === undefined ||
    result.status !== status.exitCode
  ) {
    operational("sandbox-setup-failed");
  }
  if ([126, 127].includes(status.exitCode)) {
    if (executionKind === "canary") {
      operational("toolchain-canary-tool-exec-failed");
    }
    safety("candidate-tool-exec-failed");
  }
  if (status.exitCode >= 128) {
    if (executionKind === "canary") {
      operational("toolchain-canary-signal-failure");
    }
    safety("candidate-signal-failure");
  }
  return { completedNormally: true, exitCode: status.exitCode };
}

function commandPassed(result) {
  return result.completedNormally && result.exitCode === 0;
}

