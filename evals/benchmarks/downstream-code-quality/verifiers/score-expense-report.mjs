function requireNixTool(name, environmentVariable, expectedPath) {
  if (process.platform !== "linux") {
    operational("linux-sandbox-required");
  }
  const configured = process.env[environmentVariable];
  if (!configured || !path.isAbsolute(configured)) {
    operational(`${name}-path-missing`);
  }
  let executable;
  try {
    fs.accessSync(configured, fs.constants.X_OK);
    executable = fs.realpathSync(configured);
  } catch {
    operational(`${name}-unavailable`);
  }
  if (!expectedPath.test(executable)) operational(`${name}-not-flake-selected`);
  return executable;
}

function requirePinnedNixTool(
  name,
  environmentVariable,
  expectedSha256Variable,
  expectedPath,
) {
  const executable = requireNixTool(name, environmentVariable, expectedPath);
  const expectedSha256 = process.env[expectedSha256Variable];
  if (!/^[0-9a-f]{64}$/u.test(expectedSha256 ?? "")) {
    operational(`${name}-sha256-missing`);
  }
  let metadata;
  let contents;
  try {
    metadata = fs.lstatSync(executable);
    contents = fs.readFileSync(executable);
  } catch {
    operational(`${name}-unavailable`);
  }
  if (
    !metadata.isFile() ||
    metadata.isSymbolicLink() ||
    metadata.uid !== 0 ||
    (metadata.mode & 0o022) !== 0 ||
    sha256(contents) !== expectedSha256
  ) {
    operational(`${name}-integrity-mismatch`);
  }
  return executable;
}

function nixToolchain() {
  const directories = [];
  for (const entry of (process.env.PATH || "").split(path.delimiter)) {
    if (!path.isAbsolute(entry)) continue;
    try {
      const canonical = fs.realpathSync(entry);
      if (
        nixBinDirectoryPattern.test(canonical) &&
        !directories.includes(canonical)
      ) {
        directories.push(canonical);
      }
    } catch {
      // Caller PATH entries outside the selected Nix closure are irrelevant.
    }
  }
  if (directories.length === 0) operational("nix-tool-path-missing");

  const executables = {};
  for (const name of [
    "bash",
    "cargo",
    "cargo-clippy",
    "cargo-fmt",
    "cc",
    "cp",
    "env",
    "git",
    "rustc",
    "rustdoc",
    "rustfmt",
  ]) {
    let executable;
    for (const directory of directories) {
      const candidate = path.join(directory, name);
      try {
        fs.accessSync(candidate, fs.constants.X_OK);
        const canonical = fs.realpathSync(candidate);
        if (
          nixExecutablePattern.test(candidate) &&
          nixExecutablePattern.test(canonical)
        ) {
          // Preserve the selected command's basename for Nix multi-call tools
          // such as coreutils while still validating its immutable target.
          executable = candidate;
          break;
        }
      } catch {
        // Try the next Nix-store PATH directory.
      }
    }
    if (!executable) operational(`nix-tool-${name}-missing`);
    executables[name] = executable;
  }
  return {
    ...executables,
    path: [...new Set(Object.values(executables).map(path.dirname))].join(
      path.delimiter,
    ),
  };
}

function gitHead(workspace, git) {
  const result = spawnSync(
    git,
    ["-C", workspace, "rev-parse", "--verify", "HEAD^{commit}"],
    {
      encoding: "utf8",
      env: {
        GIT_CONFIG_GLOBAL: "/dev/null",
        GIT_CONFIG_NOSYSTEM: "1",
        GIT_CONFIG_SYSTEM: "/dev/null",
        GIT_NO_REPLACE_OBJECTS: "1",
        GIT_OPTIONAL_LOCKS: "0",
        HOME: workspace,
        LANG: "C.UTF-8",
        LC_ALL: "C.UTF-8",
        PATH: path.dirname(git),
      },
      killSignal: "SIGKILL",
      maxBuffer: 16 * 1024,
      stdio: ["ignore", "pipe", "pipe"],
      timeout: 5_000,
    },
  );
  if (result.error || result.status !== 0) provenance("invalid-git-head");
  const head = result.stdout.trim();
  if (!/^(?:[0-9a-f]{40}|[0-9a-f]{64})$/.test(head)) {
    provenance("invalid-git-head");
  }
  return head;
}

function requireContainedGitTree(root) {
  let entries = 0;
  let bytes = 0;

  function visit(directory) {
    const stat = lstat(
      directory,
      "provenance-failure",
      "invalid-git-object-store",
    );
    if (!stat?.isDirectory() || stat.isSymbolicLink()) {
      provenance("invalid-git-object-store");
    }
    let canonical;
    try {
      canonical = fs.realpathSync(directory);
    } catch {
      provenance("invalid-git-object-store");
    }
    const relative = path.relative(root, canonical);
    if (
      relative === ".." ||
      relative.startsWith(`..${path.sep}`) ||
      path.isAbsolute(relative)
    ) {
      provenance("external-git-object-store");
    }
    for (const name of readDirectoryEntries(
      directory,
      2_048,
      "provenance-failure",
      "git-object-entry-limit",
    )) {
      entries += 1;
      if (entries > 2_048) provenance("git-object-entry-limit");
      const child = path.join(directory, name);
      const childStat = lstat(
        child,
        "provenance-failure",
        "invalid-git-object-store",
      );
      if (childStat?.isDirectory() && !childStat.isSymbolicLink()) {
        visit(child);
      } else if (
        childStat?.isFile() &&
        !childStat.isSymbolicLink() &&
        childStat.nlink === 1
      ) {
        bytes += childStat.size;
        if (bytes > 64 * 1024 * 1024) {
          provenance("git-object-size-limit");
        }
      } else {
        provenance("invalid-git-object-store");
      }
    }
  }

  requireRealDirectory(root, "provenance-failure", "invalid-git-object-store");
  visit(root);
}

