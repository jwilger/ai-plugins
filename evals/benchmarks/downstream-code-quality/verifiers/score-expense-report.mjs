function lstat(candidate, failureKind, code) {
  try {
    return fs.lstatSync(candidate, { throwIfNoEntry: false });
  } catch {
    fail(failureKind, code);
  }
}

function readRegularFile(candidate, maximumBytes, failureKind, code) {
  const before = lstat(candidate, failureKind, code);
  if (
    !before?.isFile() ||
    before.isSymbolicLink() ||
    before.nlink !== 1 ||
    before.size > maximumBytes
  ) {
    fail(failureKind, code);
  }

  let descriptor;
  try {
    descriptor = fs.openSync(
      candidate,
      fs.constants.O_RDONLY | fs.constants.O_NOFOLLOW,
    );
    const opened = fs.fstatSync(descriptor);
    if (
      !opened.isFile() ||
      opened.nlink !== 1 ||
      opened.dev !== before.dev ||
      opened.ino !== before.ino ||
      opened.size > maximumBytes
    ) {
      fail(failureKind, code);
    }
    const contents = fs.readFileSync(descriptor);
    if (contents.byteLength > maximumBytes) fail(failureKind, code);
    return contents;
  } catch (error) {
    if (error instanceof ScorerError) throw error;
    fail(failureKind, code);
  } finally {
    if (descriptor !== undefined) {
      try {
        fs.closeSync(descriptor);
      } catch {
        // A close failure cannot make captured immutable bytes unsafe to use.
      }
    }
  }
}

function requireRealDirectory(candidate, failureKind, code) {
  const stat = lstat(candidate, failureKind, code);
  if (!stat?.isDirectory() || stat.isSymbolicLink()) {
    fail(failureKind, code);
  }
  let canonical;
  try {
    canonical = fs.realpathSync(candidate);
  } catch {
    fail(failureKind, code);
  }
  if (canonical !== path.resolve(candidate)) fail(failureKind, code);
  return canonical;
}

function pathsOverlap(first, second) {
  const firstToSecond = path.relative(first, second);
  const secondToFirst = path.relative(second, first);
  const contains = (relative) =>
    relative === "" ||
    (relative !== ".." &&
      !relative.startsWith(`..${path.sep}`) &&
      !path.isAbsolute(relative));
  return contains(firstToSecond) || contains(secondToFirst);
}

