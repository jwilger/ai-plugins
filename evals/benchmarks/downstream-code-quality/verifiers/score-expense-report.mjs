function score(args, tools) {
  const verifierCompositionSha256 = verifierComposition().sha256;
  requirePreparedWorkspace(args.workspace, args.baselineOid, tools.git);
  const trusted = captureTrustedFixture(args.trustedFixtureDigest);
  const metadata = {
    trustedFixtureSha256: trusted.digest,
    verifierCompositionSha256,
  };
  let candidate;
  try {
    candidate = inspectCandidate(args.workspace, trusted);
  } catch (error) {
    if (error instanceof ScorerError && error.kind === "safety-failure") {
      return safetyReport(metadata);
    }
    throw error;
  }

  const temporaryParent = verifierTempRoot(args.workspace);
  let temporaryRoot;
  let report;
  let scoringError;
  try {
    temporaryRoot = fs.mkdtempSync(
      path.join(temporaryParent, "expense-report-score-"),
    );
    fs.chmodSync(temporaryRoot, 0o700);
    const canaryRoot = path.join(temporaryRoot, "toolchain-canary");
    const candidateRoot = path.join(temporaryRoot, "candidate");
    const baselineRoot = path.join(temporaryRoot, "baseline-replay");
    runToolchainCanary(canaryRoot, tools);
    prepareBuildRoot(candidateRoot, trusted.controls, candidate.sources);
    const artifactDirectory = path.join(candidateRoot, "artifacts");
    fs.mkdirSync(artifactDirectory, { mode: 0o700 });
    const rebuiltExecutable = path.join(artifactDirectory, "expense-report");

    const target = {
      bwrap: tools.bwrap,
      buildRoot: candidateRoot,
      nixStoreClosure: tools.nixStoreClosure,
      prlimit: tools.prlimit,
      toolchain: tools,
    };
    const format = commandPassed(
      runCargo(target, ["fmt", "--all", "--", "--check"]),
    );
    const clippy = commandPassed(
      runCargo(target, [
        "clippy",
        "--locked",
        "--offline",
        "--all-targets",
        "--",
        "-D",
        "warnings",
      ]),
    );
    const regressionTests = commandPassed(
      runCargo(target, ["test", "--locked", "--offline"]),
    );
    const sourceRebuild = commandPassed(
      runCargo(target, ["build", "--locked", "--offline"], artifactDirectory),
    );
    const blackBoxBehavior = sourceRebuild
      ? runPublicVerifier(candidateRoot, rebuiltExecutable, target)
      : false;

    const candidateTests = new Map(
      [...candidate.sources].filter(([relative]) =>
        relative.startsWith(`tests${path.sep}`),
      ),
    );
    const pristineSource = new Map(
      [...trusted.sources].filter(([relative]) =>
        relative.startsWith(`src${path.sep}`),
      ),
    );
    prepareBuildRoot(
      baselineRoot,
      trusted.controls,
      new Map([...pristineSource, ...candidateTests]),
    );
    const baselineResult = runCargo({ ...target, buildRoot: baselineRoot }, [
      "test",
      "--locked",
      "--offline",
    ]);
    const baselineRegressionReplay =
      regressionTests &&
      baselineResult.completedNormally &&
      baselineResult.exitCode > 0 &&
      baselineResult.exitCode < 128;

    const gates = {
      "source-rebuild": sourceRebuild,
      "black-box-behavior": blackBoxBehavior,
      "regression-tests": regressionTests,
      "baseline-regression-replay": baselineRegressionReplay,
      format,
      clippy,
      "diff-scope": true,
      safety: true,
    };
    const pass = Object.values(gates).every(Boolean);
    report = {
      schemaVersion: 1,
      verifier: "expense-report-trusted-source",
      pass,
      outcomeClass: pass ? "pass" : "candidate-failure",
      ...metadata,
      changeEvidence: candidate.changeEvidence,
      gates,
    };
  } catch (error) {
    if (error instanceof ScorerError && error.kind === "safety-failure") {
      report = safetyReport(metadata, candidate.changeEvidence);
    } else {
      scoringError = error;
    }
  } finally {
    if (temporaryRoot) {
      try {
        fs.rmSync(temporaryRoot, { force: true, recursive: true });
      } catch {
        scoringError = new ScorerError(
          "operational-failure",
          "temporary-cleanup-failed",
        );
      }
    }
  }
  if (scoringError) throw scoringError;
  return report;
}

