function cleanupVerifierTemporaryState() {
  const root = process.env.CODE_QUALITY_VERIFIER_TMP_ROOT;
  try {
    const metadata = fs.lstatSync(root);
    if (
      !path.isAbsolute(root) ||
      path.resolve(root) !== root ||
      fs.realpathSync(root) !== root ||
      !metadata.isDirectory() ||
      metadata.isSymbolicLink() ||
      metadata.uid !== process.getuid()
    ) {
      return false;
    }
    const entries = fs.readdirSync(root);
    if (entries.length > 16) return false;
    for (const name of entries) {
      if (!/^expense-report-score-[A-Za-z0-9]{6}$/u.test(name)) return false;
      const candidate = path.join(root, name);
      const candidateMetadata = fs.lstatSync(candidate);
      if (
        !candidateMetadata.isDirectory() ||
        candidateMetadata.isSymbolicLink() ||
        candidateMetadata.uid !== process.getuid()
      ) {
        return false;
      }
      fs.rmSync(candidate, { recursive: true });
    }
    return fs.readdirSync(root).length === 0;
  } catch {
    return false;
  }
}

function requireExactScopedEnvironment(expected) {
  const observedNames = Object.keys(process.env).sort();
  const expectedNames = Object.keys(expected).sort();
  if (
    JSON.stringify(observedNames) !== JSON.stringify(expectedNames) ||
    expectedNames.some((name) => process.env[name] !== expected[name])
  ) {
    operational("resource-scope-environment-invalid");
  }
}

function classifyScorerError(error) {
  if (error instanceof NixStoreClosureError) {
    return new ScorerError("operational-failure", error.code);
  }
  if (error instanceof VerifierCompositionError) {
    return new ScorerError("operational-failure", error.code);
  }
  return error instanceof ScorerError
    ? error
    : new ScorerError("operational-failure", "unexpected");
}

function writeScorerError(error) {
  const classified = classifyScorerError(error);
  process.stderr.write(
    `score-expense-report:${classified.kind}:${classified.code}\n`,
  );
  process.exitCode = 2;
}

