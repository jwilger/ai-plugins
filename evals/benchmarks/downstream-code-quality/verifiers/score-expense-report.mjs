async function scoreInAggregateScope(argv) {
  const systemdRun = requirePinnedNixTool(
    "systemd-run",
    "CODE_QUALITY_SYSTEMD_RUN_BIN",
    "CODE_QUALITY_SYSTEMD_RUN_EXPECTED_SHA256",
    /^\/nix\/store\/[0-9a-z]{32}-systemd-[^/]+\/bin\/systemd-run$/,
  );
  const systemdRuntimeDirectory = requireSystemdRuntimeDirectory();
  const toolchain = nixToolchain();
  const scopedEnvironment = exactScopedEnvironment(toolchain);
  const unit = `ai-plugins-code-quality-scorer-${process.pid}-${crypto
    .randomBytes(8)
    .toString("hex")}`;
  const child = spawn(
    systemdRun,
    [
      "--user",
      "--scope",
      "--quiet",
      "--collect",
      "--expand-environment=false",
      `--unit=${unit}`,
      "--property=MemoryMax=8589934592",
      "--property=MemorySwapMax=0",
      "--property=TasksMax=512",
      "--property=CPUQuota=400%",
      "--property=OOMPolicy=kill",
      "--property=KillMode=control-group",
      "--",
      toolchain.env,
      "-i",
      ...Object.entries(scopedEnvironment).map(
        ([name, value]) => `${name}=${value}`,
      ),
      process.execPath,
      import.meta.filename,
      aggregateScopeChildArgument,
      unit,
      ...argv,
    ],
    {
      detached: true,
      env: {
        LANG: "C.UTF-8",
        LC_ALL: "C.UTF-8",
        PATH: path.dirname(systemdRun),
        XDG_RUNTIME_DIR: systemdRuntimeDirectory,
      },
      stdio: ["ignore", "pipe", "pipe"],
    },
  );

  await new Promise((resolve) => {
    const stdout = [];
    const stderr = [];
    let outputBytes = 0;
    let terminationReason;
    let forceKillTimer;

    function signalChild(signal) {
      try {
        process.kill(-child.pid, signal);
      } catch {
        try {
          child.kill(signal);
        } catch {
          // The aggregate scope has already exited.
        }
      }
    }

    function terminate(reason) {
      if (terminationReason) return;
      terminationReason = reason;
      signalChild("SIGTERM");
      forceKillTimer = setTimeout(() => signalChild("SIGKILL"), 5_000);
      forceKillTimer.unref();
    }

    const signalHandlers = new Map();
    for (const signal of ["SIGHUP", "SIGINT", "SIGTERM"]) {
      const handler = () => terminate("cancelled");
      signalHandlers.set(signal, handler);
      process.once(signal, handler);
    }
    const timeout = setTimeout(
      () => terminate("timeout"),
      aggregateScopeLimits.timeoutMilliseconds,
    );
    timeout.unref();

    function collect(chunks, chunk) {
      outputBytes += Buffer.byteLength(chunk);
      if (outputBytes > aggregateScopeLimits.outputBytes) {
        terminate("output-limit");
        return;
      }
      chunks.push(chunk);
    }
    child.stdout.setEncoding("utf8");
    child.stderr.setEncoding("utf8");
    child.stdout.on("data", (chunk) => collect(stdout, chunk));
    child.stderr.on("data", (chunk) => collect(stderr, chunk));
    child.on("error", () => terminate("spawn-failed"));
    child.on("close", (status, signal) => {
      clearTimeout(timeout);
      if (forceKillTimer) clearTimeout(forceKillTimer);
      for (const [name, handler] of signalHandlers) {
        process.removeListener(name, handler);
      }
      const cleanupSucceeded = cleanupVerifierTemporaryState();
      if (!cleanupSucceeded) {
        writeScorerError(
          new ScorerError(
            "operational-failure",
            "resource-scope-cleanup-failed",
          ),
        );
      } else if (terminationReason) {
        writeScorerError(
          new ScorerError(
            "operational-failure",
            `resource-scope-${terminationReason}`,
          ),
        );
      } else if (status === 0 && signal === null && stderr.join("") === "") {
        process.stdout.write(stdout.join(""));
      } else if (
        status === 2 &&
        signal === null &&
        stdout.join("") === "" &&
        /^score-expense-report:(?:operational-failure|provenance-failure):[a-z0-9-]+\n$/u.test(
          stderr.join(""),
        )
      ) {
        process.stderr.write(stderr.join(""));
        process.exitCode = 2;
      } else {
        writeScorerError(
          new ScorerError("operational-failure", "resource-scope-failed"),
        );
      }
      resolve();
    });
  });
}

function calculateChangeEvidence(baselineSources, candidateSources) {
  const counts = { added: 0, deleted: 0, modified: 0 };
  const diffEntries = [];
  const paths = [
    ...new Set([...baselineSources.keys(), ...candidateSources.keys()]),
  ].sort((left, right) => Buffer.from(left).compare(Buffer.from(right)));
  for (const relative of paths) {
    const baseline = baselineSources.get(relative);
    const candidate = candidateSources.get(relative);
    if (baseline && candidate && baseline.equals(candidate)) continue;
    const status = baseline ? (candidate ? "modified" : "deleted") : "added";
    counts[status] += 1;
    diffEntries.push([
      `${status}\0${relative}`,
      Buffer.from(
        `${baseline ? sha256(baseline) : "-"}\0${
          candidate ? sha256(candidate) : "-"
        }`,
      ),
    ]);
  }
  return {
    sourceFileCount: candidateSources.size,
    sourceByteCount: [...candidateSources.values()].reduce(
      (total, contents) => total + contents.byteLength,
      0,
    ),
    addedFileCount: counts.added,
    modifiedFileCount: counts.modified,
    deletedFileCount: counts.deleted,
    changedFileCount: counts.added + counts.modified + counts.deleted,
    candidateTreeSha256: sha256TreeSnapshot(candidateSources),
    diffSha256: sha256FramedEntries(
      "ai-plugins-code-quality-diff-v1",
      diffEntries,
    ),
  };
}

function inspectCandidate(workspace, trusted) {
  const topLevelEntries = readDirectoryEntries(
    workspace,
    32,
    "safety-failure",
    "workspace-entry-limit",
  );
  const allowed = new Set([".git", ...controlFiles, ...sourceRoots]);
  for (const name of topLevelEntries) {
    const absolute = path.join(workspace, name);
    const stat = lstat(absolute, "safety-failure", "invalid-workspace-entry");
    if (ignoredOperationalDirectories.has(name)) {
      if (!stat?.isDirectory() || stat.isSymbolicLink()) {
        safety("invalid-operational-directory");
      }
      continue;
    }
    if (!allowed.has(name)) safety("out-of-scope-workspace-entry");
  }

  for (const [relative, expected] of trusted.controls) {
    const observed = readRegularFile(
      path.join(workspace, relative),
      256 * 1024,
      "safety-failure",
      "candidate-control-invalid",
    );
    if (!observed.equals(expected)) safety("candidate-control-changed");
  }

  const sources = captureSourceTree(workspace);
  return {
    changeEvidence: calculateChangeEvidence(trusted.sources, sources),
    sources,
  };
}

